package escseq;

public class Escseq {
    public static void main(String[] args) {
        int num = 7;
        
        System.out.println(num);        
        System.out.println("\t" + num * 2);        
        System.out.println(num * 3);        
        System.out.println("\t" + num * 4);        
        System.out.println(num * 5);
    }
}
